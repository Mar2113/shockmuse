        <h1>Declaración de Accesibilidad</h1>
        <p>El Ministerio de Hacienda y Función Pública se ha comprometido a hacer accesibles sus sitios web de conformidad con el Real Decreto 1112/2018, de 7 de septiembre, sobre accesibilidad de los sitios web y aplicaciones para dispositivos móviles del sector público (en adelante, Real Decreto 1112/2018, de 7 de septiembre).</p>
        <p>La presente declaración de accesibilidad se aplica al sitio web <a href="https://www.hacienda.gob.es">https://www.hacienda.gob.es</a> (los subdominios de este sitio web tendrán sus propias declaraciones de accesibilidad).</p>
        <p style="font-weight: bold;">Situación de cumplimiento</p>
        <p>Este sitio web es parcialmente conforme con el Real Decreto 1112/2018, de 7 de septiembre, debido a la falta de conformidad de los aspectos que se indican a continuación:</p>
        <ul>
            <li>Falta de conformidad con el Real Decreto 1112/2018, de 7 de septiembre: podrían existir fallos puntuales de edición en alguna página web, tanto en contenidos HTML como en documentos finales, publicados en fecha posterior al 20 de septiembre de 2018 (fecha de entrada en vigor del Real Decreto 1112/2018, de 7 de septiembre). Concretamente puede haber errores en aquellos documentos que contienen gráficos, tablas complejas o en aquellos firmados electrónicamente antes de su revisión de accesibilidad.</li>
            <li>Carga desproporcionada: no aplica.</li>
            <li>El contenido no entra dentro del ámbito de la legislación aplicable: Existen archivos ofimáticos en PDF u otros formatos publicados antes del 20 de septiembre de 2018 que no cumplen en su totalidad todos los requisitos de accesibilidad. También pueden estar en esa situación contenidos elaborados por terceros no financiados, ni desarrollados, ni bajo el control del responsable de este sitio web.</li>
        </ul>
        <p style="font-weight: bold;">Preparación de la presente declaración de accesibilidad</p>
        <p>La presente declaración fue preparada el 11 de Marzo de 2024.</p>
        <p>El método empleado para preparar la declaración ha sido una autoevaluación llevada a cabo por el propio organismo.</p>
        <p>Última revisión de la declaración: 14 de Marzo de 2024.</p>
        <p style="font-weight: bold;">Observaciones y datos de contacto</p>
        <p>Puede realizar comunicaciones sobre requisitos de accesibilidad (artículo 10.2.a) del Real Decreto 1112/2018, de 7 de septiembre) como, por ejemplo:</p>
        <ul>
            <li>Informar sobre cualquier posible incumplimiento por parte de este sitio web.</li>
            <li>Transmitir otras dificultades de acceso al contenido.</li>
            <li>Formular cualquier otra consulta o sugerencia de mejora relativa a la accesibilidad del sitio web.</li>
        </ul>
        <p>Para más información y contactar: <a href="mailto:accesibilidadweb@hacienda.gob.es">accesibilidadweb@hacienda.gob.es</a></p>
        <p>También puede solicitar en formato accesible información que no cumpla con los requisitos de accesibilidad bien por estar excluida, bien por estar exenta por carga desproporcionada; o formular una queja por incumplimiento de los requisitos de accesibilidad. <a href="#">Enlace al procedimiento de solicitud de información accesible y quejas</a> del artículo 12 del Real Decreto 1112/2018, de 7 de septiembre.</p>
        <p>Las comunicaciones, solicitudes de información accesible y quejas debe dirigirlas a la Subdirección General de Servicios Web, Transparencia y Protección de Datos (Secretaría General Técnica), como unidad responsable de los contenidos de este sitio web.</p>
        <p style="font-weight: bold;">Procedimiento de aplicación</p>
        <p>A través de este procedimiento podrá iniciar una reclamación para conocer y oponerse a los motivos de la desestimación de una solicitud de información accesible o queja, instar la adopción de las medidas oportunas en el caso de no estar de acuerdo con la decisión adoptada, o exponer las razones por las que se considera que la respuesta no cumple con los requisitos exigidos.</p>
        <p><a href="#">Enlace al procedimiento de reclamación</a>, que debe dirigir a la Subdirección General de Servicios Web, Transparencia y Protección de Datos, como Unidad responsable de accesibilidad de este Ministerio, o si la reclamación es por una actuación de dicha Unidad, al superior jerárquico de ésta.</p>
        <p style="font-weight: bold;">Contenido opcional</p>
        <p>La versión actualmente visible de este sitio web es de Marzo de 2024 y en esa fecha se hizo la revisión del nivel de accesibilidad vigente en aquel momento.</p>
        <p>A partir de dicha fecha se llevan a cabo revisiones parciales diarias del contenido web nuevo o modificado, tanto de las plantillas como de las páginas y documentos finales publicados, a fin de asegurar el cumplimiento de los requerimientos de accesibilidad de la Norma UNE-EN 301549:2022, considerando las excepciones del Real Decreto 1112/2018, de 7 de septiembre.</p>
        <p>Adicionalmente, este sitio web es revisado por el Observatorio de Accesibilidad Web (OAW), habiéndose realizado la última revisión en Marzo 2024.</p>
        <p>Entre otras se adoptan las siguientes medidas para facilitar la accesibilidad:</p>
        <ul>
            <li>Utilización de texto alternativo en las imágenes</li>
            <li>Los enlaces ofrecen detalles de la función o destino del hipervínculo</li>
            <li>Uso de los estándares del W3C: XHTML 1.0, CSS 3.0, WAI AA.</li>
        </ul>
